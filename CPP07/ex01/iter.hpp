#ifndef ITER_HPP
#define ITER_HPP

/******************************************************************************/
/*                                 Includes                                   */
/******************************************************************************/

#include <iostream>
#include <string>
#include <cstdio>

/******************************************************************************/
/*                                 Functions                                  */
/******************************************************************************/

template<typename T, typename F>
void    iter(T *array, int lenghtArray, F function)
{
    int i = 0;

    while (i < lenghtArray)
    {
        function(array[i]);
        i++;
    }
}

#endif